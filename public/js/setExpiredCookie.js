const setExpirationSession = (nextExpirationSession,uid) => {
    fetch(`http://localhost:4000/api/auth/expired/${uid}`,{
        method:"PATCH",
        headers:{
            "Content-Type":"application/json"
        },
        body:JSON.stringify({
            nextExpirationSession
        })
    })
    .then(response=>response.json())
    .then(result=>{
        if(!result.status){
            console.log(result);
        }
    })
    .catch(err=>{
        console.log(err);
    })
}